public class Main {
    public static void main(String[] args) {
        int num = 10, i = 1;
        long factorial = 1;
        while (i <= num) {
            factorial *= i;
            i++;
        System.out.println(factorial);
        }
        }
}
        /*
        Вычислить факториал целых чисел от 0 до 10 с помощью цикла while.
         */
